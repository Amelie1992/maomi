public class DrawPrizeBean
{
	/**
	 * 获奖金额
	 */
	private double drawMoney;
	
	/**
	 * 获奖概率
	 */
	private double drawProb;

	public double getDrawMoney()
	{
		return drawMoney;
	}

	public void setDrawMoney(double drawMoney)
	{
		this.drawMoney = drawMoney;
	}

	public double getDrawProb()
	{
		return drawProb;
	}

	public void setDrawProb(double drawProb)
	{
		this.drawProb = drawProb;
	}

	/**
	 * 
	 */
	public DrawPrizeBean()
	{
		super();
		// TODO Auto-generated constructor stub
	}

	/**
	 * @param drawMoney
	 * @param drawProb
	 */
	public DrawPrizeBean(double drawMoney, double drawProb)
	{
		super();
		this.drawMoney = drawMoney;
		this.drawProb = drawProb;
	}

	@Override
	public String toString()
	{
		return "DRAWPRIZEBean [drawMoney=" + drawMoney + ", drawProb=" + drawProb + "]";
	}
	
	

}
