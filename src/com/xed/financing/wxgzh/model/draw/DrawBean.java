public class DrawBean
{
	/**
	 * 获奖ID
	 */
	private String drawId;
	
	/**
	 * 用户ID
	 */
	private String accountId;
	
	/**
	 * 用户手机号
	 */
	private String telephone;
	
	/**
	 * 奖项名次
	 */
	private String drawRank;
	
	/**
	 * 奖项内容
	 */
	private String drawContent;
	
	/**
	 * 获奖时间
	 */
	private String drawTime;
	
	/**
	 * 是否领取
	 */
	private String isGrant;
	
	/**
	 * 是否抽奖页面查询
	 */
	private String isAward;

	public String getDrawId()
	{
		return drawId;
	}

	public void setDrawId(String drawId)
	{
		this.drawId = drawId;
	}

	public String getAccountId()
	{
		return accountId;
	}

	public void setAccountId(String accountId)
	{
		this.accountId = accountId;
	}

	public String getTelephone()
	{
		return telephone;
	}

	public void setTelephone(String telephone)
	{
		this.telephone = telephone;
	}

	public String getDrawContent()
	{
		return drawContent;
	}

	public void setDrawContent(String drawContent)
	{
		this.drawContent = drawContent;
	}

	public String getDrawTime()
	{
		return drawTime;
	}

	public void setDrawTime(String drawTime)
	{
		this.drawTime = drawTime;
	}

	public String getIsGrant()
	{
		return isGrant;
	}

	public void setIsGrant(String isGrant)
	{
		this.isGrant = isGrant;
	}
	
	public String getDrawRank()
	{
		return drawRank;
	}

	public void setDrawRank(String drawRank)
	{
		this.drawRank = drawRank;
	}
	
	public String getIsAward()
	{
		return isAward;
	}

	public void setIsAward(String isAward)
	{
		this.isAward = isAward;
	}

	/**
	 * 
	 */
	public DrawBean()
	{
		super();
		// TODO Auto-generated constructor stub
	}

	/**
	 * @param drawId
	 * @param accountId
	 * @param telephone
	 * @param drawRank
	 * @param drawContent
	 * @param drawTime
	 * @param isGrant
	 * @param isAward
	 */
	public DrawBean(String drawId, String accountId, String telephone, String drawRank, String drawContent,
			String drawTime, String isGrant, String isAward)
	{
		super();
		this.drawId = drawId;
		this.accountId = accountId;
		this.telephone = telephone;
		this.drawRank = drawRank;
		this.drawContent = drawContent;
		this.drawTime = drawTime;
		this.isGrant = isGrant;
		this.isAward = isAward;
	}

}
