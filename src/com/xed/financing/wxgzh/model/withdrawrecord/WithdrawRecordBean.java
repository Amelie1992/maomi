package com.xed.financing.wxgzh.model.withdrawrecord;

/**
 * @className:com.xed.financing.wxgzh.model.withdrawrecord.WithdrawRecordBean
 * @description:提现过渡记录
 * @version:v1.0.0 
 * @date:2017年9月8日 上午9:45:46
 * @author:Elias Zheng
 */
public class WithdrawRecordBean
{
	/**
	 * 过渡ID
	 */
	private String withdrawId;
	
	/**
	 * 用户ID
	 */
	private String accountId;
	
	/**
	 * 提现金额
	 */
	private String money;
	
	/**
	 * 申请时间
	 */
	private String applyTime;
	
	/**
	 * 到账时间
	 */
	private String arrivalTime;
	
	/**
	 * 提现状态
	 */
	private String withdrawStatus;
	
	/**
	 * 说明
	 */
	private String remark;
	
	/**
	 * 是否提现成功
	 */
	private String isArrival;

	public String getWithdrawId()
	{
		return withdrawId;
	}

	public void setWithdrawId(String withdrawId)
	{
		this.withdrawId = withdrawId;
	}

	public String getAccountId()
	{
		return accountId;
	}

	public void setAccountId(String accountId)
	{
		this.accountId = accountId;
	}

	public String getMoney()
	{
		return money;
	}

	public void setMoney(String money)
	{
		this.money = money;
	}

	public String getApplyTime()
	{
		return applyTime;
	}

	public void setApplyTime(String applyTime)
	{
		this.applyTime = applyTime;
	}

	public String getArrivalTime()
	{
		return arrivalTime;
	}

	public void setArrivalTime(String arrivalTime)
	{
		this.arrivalTime = arrivalTime;
	}

	public String getWithdrawStatus()
	{
		return withdrawStatus;
	}

	public void setWithdrawStatus(String withdrawStatus)
	{
		this.withdrawStatus = withdrawStatus;
	}

	public String getRemark()
	{
		return remark;
	}

	public void setRemark(String remark)
	{
		this.remark = remark;
	}
	
	public String getIsArrival()
	{
		return isArrival;
	}

	public void setIsArrival(String isArrival)
	{
		this.isArrival = isArrival;
	}

	@Override
	public String toString()
	{
		return "WithdrawRecordBean [withdrawId=" + withdrawId + ", accountId=" + accountId + ", money=" + money
				+ ", applyTime=" + applyTime + ", arrivalTime=" + arrivalTime + ", withdrawStatus=" + withdrawStatus
				+ ", remark=" + remark + ", isArrival=" + isArrival + "]";
	}

	/**
	 * @param withdrawId
	 * @param accountId
	 * @param money
	 * @param applyTime
	 * @param arrivalTime
	 * @param withdrawStatus
	 * @param remark
	 * @param isArrival
	 */
	public WithdrawRecordBean(String withdrawId, String accountId, String money, String applyTime, String arrivalTime,
			String withdrawStatus, String remark, String isArrival)
	{
		super();
		this.withdrawId = withdrawId;
		this.accountId = accountId;
		this.money = money;
		this.applyTime = applyTime;
		this.arrivalTime = arrivalTime;
		this.withdrawStatus = withdrawStatus;
		this.remark = remark;
		this.isArrival = isArrival;
	}

	/**
	 * 
	 */
	public WithdrawRecordBean()
	{
		super();
		// TODO Auto-generated constructor stub
	}
	
}
