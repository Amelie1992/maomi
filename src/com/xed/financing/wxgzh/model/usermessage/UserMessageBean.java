package com.xed.financing.wxgzh.model.usermessage;

/**
 * @className:com.xed.financing.wxgzh.model.usermessage.UserMessageBean
 * @description:
 * @version:v1.0.0
 * @date:2018年5月4日 下午2:42:47
 * @author:ZhangJun
 */
public class UserMessageBean
{

	/**
	 * 消息ID
	 */
	private String msgId;

	/**
	 * 借款用户ID
	 */
	private String userId;

	/**
	 * 消息标题
	 */
	private String msgTitle;

	/**
	 * 消息内容
	 */
	private String msgContent;

	/**
	 * 是否已读(0:否 1:是)
	 */
	private String isRead;

	/**
	 * 发送时间
	 */
	private String addTime;

	/**
	 * 是否删除(0:否 1:是)
	 */
	private String isOut;

	public String getMsgId()
	{
		return msgId;
	}

	public void setMsgId(String msgId)
	{
		this.msgId = msgId;
	}

	public String getUserId()
	{
		return userId;
	}

	public void setUserId(String userId)
	{
		this.userId = userId;
	}

	public String getMsgTitle()
	{
		return msgTitle;
	}

	public void setMsgTitle(String msgTitle)
	{
		this.msgTitle = msgTitle;
	}

	public String getMsgContent()
	{
		return msgContent;
	}

	public void setMsgContent(String msgContent)
	{
		this.msgContent = msgContent;
	}

	public String getIsRead()
	{
		return isRead;
	}

	public void setIsRead(String isRead)
	{
		this.isRead = isRead;
	}

	public String getAddTime()
	{
		return addTime;
	}

	public void setAddTime(String addTime)
	{
		this.addTime = addTime;
	}

	public String getIsOut()
	{
		return isOut;
	}

	public void setIsOut(String isOut)
	{
		this.isOut = isOut;
	}

	public UserMessageBean()
	{
		super();
	}

	public UserMessageBean(String msgId, String userId, String msgTitle, String msgContent, String isRead,
			String addTime, String isOut)
	{
		super();
		this.msgId = msgId;
		this.userId = userId;
		this.msgTitle = msgTitle;
		this.msgContent = msgContent;
		this.isRead = isRead;
		this.addTime = addTime;
		this.isOut = isOut;
	}

}
