package com.xed.financing.wxgzh.model.couponDetail;

/**
 * 优惠券详情
 * 
 * @className:com.xed.financing.wxgzh.model.couponDetail.CouponDetail
 * @description:
 * @version:v1.0.0
 * @date:2017年5月2日 下午3:56:22
 * @author:ZhangJun
 */
public class CouponDetail
{
	/**
	 * 券明细ID
	 */
	private String couDetailId;

	/**
	 * 券ID
	 */
	private String couponId;

	/**
	 * 券编码
	 */
	private String couponCode;

	/**
	 * 券编码
	 */
	private String couponMoney;

	/**
	 * 是否被领取(0:否 1:是)
	 */
	private String isReceive;

	/**
	 * 是否上下架(0:下架 1:上架)
	 */
	private String isShow;

	/**
	 * 有效期(天)(-1表示该红包没有时效性)
	 */
	private String validityDays;

	/**
	 * 领取时间
	 */
	private String receiveTime;

	/**
	 * 计息期限(-1无限制)
	 */
	private String interestDays;

	/**
	 * 计息类型(0:天 1:月 2:年)
	 */
	private String interestType;

	/**
	 * 券类型(0:增值券 1:加息券 2:体验金 3:现金券 4:提现券 5.活动券)
	 */
	private String couponType;
	
	/**
	 * 起投金额
	 */
	private String startMoney;
	
	/**
	 * 适用标类型
	 */
	private String subjectType;
	

	public String getStartMoney()
	{
		return startMoney;
	}

	public void setStartMoney(String startMoney)
	{
		this.startMoney = startMoney;
	}

	public String getSubjectType()
	{
		return subjectType;
	}

	public void setSubjectType(String subjectType)
	{
		this.subjectType = subjectType;
	}

	public String getCouponType()
	{
		return couponType;
	}

	public void setCouponType(String couponType)
	{
		this.couponType = couponType;
	}

	public String getReceiveTime()
	{
		return receiveTime;
	}

	public void setReceiveTime(String receiveTime)
	{
		this.receiveTime = receiveTime;
	}

	public String getInterestDays()
	{
		return interestDays;
	}

	public void setInterestDays(String interestDays)
	{
		this.interestDays = interestDays;
	}

	public String getInterestType()
	{
		return interestType;
	}

	public void setInterestType(String interestType)
	{
		this.interestType = interestType;
	}

	public String getCouDetailId()
	{
		return couDetailId;
	}

	public void setCouDetailId(String couDetailId)
	{
		this.couDetailId = couDetailId;
	}

	public String getCouponId()
	{
		return couponId;
	}

	public void setCouponId(String couponId)
	{
		this.couponId = couponId;
	}

	public String getCouponCode()
	{
		return couponCode;
	}

	public void setCouponCode(String couponCode)
	{
		this.couponCode = couponCode;
	}

	public String getCouponMoney()
	{
		return couponMoney;
	}

	public void setCouponMoney(String couponMoney)
	{
		this.couponMoney = couponMoney;
	}

	public String getIsReceive()
	{
		return isReceive;
	}

	public void setIsReceive(String isReceive)
	{
		this.isReceive = isReceive;
	}

	public String getIsShow()
	{
		return isShow;
	}

	public void setIsShow(String isShow)
	{
		this.isShow = isShow;
	}

	public String getValidityDays()
	{
		return validityDays;
	}

	public void setValidityDays(String validityDays)
	{
		this.validityDays = validityDays;
	}

	public CouponDetail()
	{
		super();
	}

	@Override
	public String toString()
	{
		return "CouponDetail [couDetailId=" + couDetailId + ", couponId=" + couponId + ", couponCode=" + couponCode
				+ ", couponMoney=" + couponMoney + ", isReceive=" + isReceive + ", isShow=" + isShow
				+ ", validityDays=" + validityDays + ", receiveTime=" + receiveTime + ", interestDays=" + interestDays
				+ ", interestType=" + interestType + "]";
	}

	public CouponDetail(String couDetailId, String couponId, String couponCode, String couponMoney, String isReceive,
			String isShow, String validityDays, String receiveTime, String interestDays, String interestType)
	{
		super();
		this.couDetailId = couDetailId;
		this.couponId = couponId;
		this.couponCode = couponCode;
		this.couponMoney = couponMoney;
		this.isReceive = isReceive;
		this.isShow = isShow;
		this.validityDays = validityDays;
		this.receiveTime = receiveTime;
		this.interestDays = interestDays;
		this.interestType = interestType;
	}

}
