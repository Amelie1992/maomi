package com.xed.financing.wxgzh.model.message;

/**
 * @className:com.xed.financing.wxgzh.model.message.CompanyMessageBean
 * @description:
 * @version:v1.0.0 
 * @date:2017年7月20日 上午10:57:36
 * @author:Qian Tao
 */
public class CompanyMessageBean
{
	/**
	 * 消息Id
	 */
	private String companyId;

	/**
	 * 用户Id
	 */
	private String accountId;

	/**
	 * 消息内容
	 */
	private String msgContent;

	/**
	 * 通知类型（0首次充值奖励，1首次投标奖励）
	 */
	private String msgType;
	
	/**
	 * 是否发送（0未发送，1已发送）
	 */
	private String isSend;

	/**
	 * 添加时间时间
	 */
	private String addTime;

	/**
	 * 发送时间
	 */
	private String sendTime;
	
	/**
	 * 用户名
	 */
	private String accountName;
	
	/**
	 * 分页查询时从该条起查询
	 */
	private Integer fromNum = 0;

	/**
	 * 分页查询时到该条为止
	 */
	private Integer toNum = 0;

	/**
	 * 每页条数
	 */
	private Integer pagenum = 10;

	private String rn;

	/**
	 * 电话号码
	 */
	private String telephone;
	
	/**
	 * 真实姓名
	 */
	private String realName;
	
	/**
	 * 银行卡号
	 */
	private String bankCard;

	public String getCompanyId()
	{
		return companyId;
	}

	public void setCompanyId(String companyId)
	{
		this.companyId = companyId;
	}

	public String getAccountId()
	{
		return accountId;
	}

	public void setAccountId(String accountId)
	{
		this.accountId = accountId;
	}

	public String getMsgContent()
	{
		return msgContent;
	}

	public void setMsgContent(String msgContent)
	{
		this.msgContent = msgContent;
	}

	public String getMsgType()
	{
		return msgType;
	}

	public void setMsgType(String msgType)
	{
		this.msgType = msgType;
	}

	public String getIsSend()
	{
		return isSend;
	}

	public void setIsSend(String isSend)
	{
		this.isSend = isSend;
	}

	public String getAddTime()
	{
		return addTime;
	}

	public void setAddTime(String addTime)
	{
		this.addTime = addTime;
	}

	public String getSendTime()
	{
		return sendTime;
	}

	public void setSendTime(String sendTime)
	{
		this.sendTime = sendTime;
	}

	public String getAccountName()
	{
		return accountName;
	}

	public void setAccountName(String accountName)
	{
		this.accountName = accountName;
	}

	public Integer getFromNum()
	{
		return fromNum;
	}

	public void setFromNum(Integer fromNum)
	{
		this.fromNum = fromNum;
	}

	public Integer getToNum()
	{
		return toNum;
	}

	public void setToNum(Integer toNum)
	{
		this.toNum = toNum;
	}

	public Integer getPagenum()
	{
		return pagenum;
	}

	public void setPagenum(Integer pagenum)
	{
		this.pagenum = pagenum;
	}

	public String getRn()
	{
		return rn;
	}

	public void setRn(String rn)
	{
		this.rn = rn;
	}

	public String getTelephone()
	{
		return telephone;
	}

	public void setTelephone(String telephone)
	{
		this.telephone = telephone;
	}

	public String getRealName()
	{
		return realName;
	}

	public void setRealName(String realName)
	{
		this.realName = realName;
	}

	public String getBankCard()
	{
		return bankCard;
	}

	public void setBankCard(String bankCard)
	{
		this.bankCard = bankCard;
	}
}
