package com.xed.financing.wxgzh.model.activityrecord;

/**
 * 集字记录
 * 
 * @className:com.xed.financing.wxgzh.model.activityrecord.ActivityRecordBean
 * @description:
 * @version:v1.0.0
 * @date:2017年4月7日 上午11:13:04
 * @author:ZhangJun
 */
public class ActivityRecordBean
{
	/**
	 * 活动ID
	 */
	private String activityId;

	/**
	 * 用户ID
	 */
	private String accountId;

	/**
	 * 获得时间
	 */
	private String gainTime;

	/**
	 * 活动类型(0:集字活动)
	 */
	private String activityType;

	/**
	 * 获取方式(0:签到 1:投资爆款 2:鱼干抽奖 3:购买鱼干 4:债权承接)
	 */
	private String gainType;

	/**
	 * 获得内容
	 */
	private String gainContent;

	/**
	 * 编码(1:牛 2:郎 3:织 4:女)
	 */
	private String gainCode;

	/**
	 * 是否使用(0:否 1:是)
	 */
	private String isUsed;

	public String getActivityId()
	{
		return activityId;
	}

	public void setActivityId(String activityId)
	{
		this.activityId = activityId;
	}

	public String getAccountId()
	{
		return accountId;
	}

	public void setAccountId(String accountId)
	{
		this.accountId = accountId;
	}

	public String getGainTime()
	{
		return gainTime;
	}

	public void setGainTime(String gainTime)
	{
		this.gainTime = gainTime;
	}

	public String getActivityType()
	{
		return activityType;
	}

	public void setActivityType(String activityType)
	{
		this.activityType = activityType;
	}

	public String getGainType()
	{
		return gainType;
	}

	public void setGainType(String gainType)
	{
		this.gainType = gainType;
	}

	public String getGainContent()
	{
		return gainContent;
	}

	public void setGainContent(String gainContent)
	{
		this.gainContent = gainContent;
	}

	public String getGainCode()
	{
		return gainCode;
	}

	public void setGainCode(String gainCode)
	{
		this.gainCode = gainCode;
	}

	public String getIsUsed()
	{
		return isUsed;
	}

	public void setIsUsed(String isUsed)
	{
		this.isUsed = isUsed;
	}

	public ActivityRecordBean(String activityId, String accountId, String gainTime, String activityType,
			String gainType, String gainContent, String gainCode, String isUsed)
	{
		super();
		this.activityId = activityId;
		this.accountId = accountId;
		this.gainTime = gainTime;
		this.activityType = activityType;
		this.gainType = gainType;
		this.gainContent = gainContent;
		this.gainCode = gainCode;
		this.isUsed = isUsed;
	}

	public ActivityRecordBean()
	{
		super();
	}

}
